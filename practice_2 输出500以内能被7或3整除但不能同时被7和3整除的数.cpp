#include<iostream>
using namespace std;

int main()
{
//	求出500以内
//	能被3整除或能被7整除
//	但不能同时被3和7整除的数
	int n;
	for(int n=0;n<=501;n++){
		if(n%3==0 && n%7==0){
			continue;
		}
		if(n%3==0 || n%7==0){
			cout << n << " ";
		}
	}
//
//
//
//
//
//	
//	
//	如果n%3==0或n%7==0
//	输出
//	如果n%3==0且n%7==0
//	不输出
//	n=3
//	
//	如果n%3==0或n%7==0
//	输出
//	如果n%3==0且n%7==0
//	不输出
//	n=4
//	如果n%3==0或n%7==0
//	输出
//	如果n%3==0且n%7==0
//	不输出
	return 0;
}
